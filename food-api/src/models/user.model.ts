import { Schema, model } from "mongoose";

const UserSchema = new Schema({
  profileEmoji: {
    type: String,
    default: "☺️",
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    enum: ["Admin", "User"],
    default: "User",
  },
  orders: {
    type: [Schema.Types.ObjectId],
    ref: "FoodOrder",
    default: [],
  },
  totalPrice: {
    type: Number,
    default: 0,
  },
  isVerified: {
    type: Boolean,
    default: false,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

export const UserModel = model("User", UserSchema);
